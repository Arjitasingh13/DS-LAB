struct node {
    int data;
    struct node* next;
};

typedef struct node* NODE;

NODE createNode(int x) {
	
	NODE newNode;
	newNode = (NODE)malloc(sizeof(struct node));
	if(newNode == NULL){
		printf("Memory allocation failed\n");
		return NULL;
	}
    newNode->data = x;
	newNode->next = NULL;
	return newNode;   
}

